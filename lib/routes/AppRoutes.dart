abstract class AppRoutes {
  static const APP_ROUTE_MAIN = _Paths.MAIN;
  static const APP_ROUTE_HOME = _Paths.HOME;
  static const APP_ROUTE_SPLASH = _Paths.SPLASH;
  static const APP_ROUTE_ONBOARDING = _Paths.ONBOARDING;
  static const APP_ROUTE_ITEM_DETAILS = _Paths.ITEM_DETAILS;
  static const APP_ROUTE_PROFILE = _Paths.PROFILE;
}

abstract class _Paths {
  static const MAIN = "/main";
  static const HOME = "/home";
  static const SPLASH = "/splash";
  static const ONBOARDING = "/onboarding";
  static const ITEM_DETAILS = "/item-details";
  static const PROFILE = "/profile";
}
